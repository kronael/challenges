IDX = {c: i for i, c in enumerate(AA)}


def solve(s, t):
    # Naive O(n^3): a single score table where each cell re-scans every possible
    # trailing-gap length instead of carrying gap state. Correct, but cubic — the trap.
    n, m = len(s), len(t)
    sub = BLOSUM62
    si = [IDX[c] for c in s]
    ti = [IDX[c] for c in t]
    NEG = float("-inf")

    def gap(length):
        return -GAP_OPEN - GAP_EXTEND * (length - 1)

    h = [[NEG] * (m + 1) for _ in range(n + 1)]
    h[0][0] = 0
    for j in range(1, m + 1):
        h[0][j] = gap(j)
    for i in range(1, n + 1):
        h[i][0] = gap(i)
    for i in range(1, n + 1):
        for j in range(1, m + 1):
            best = h[i - 1][j - 1] + sub[si[i - 1]][ti[j - 1]]
            for k in range(1, j + 1):
                v = h[i][j - k] + gap(k)
                if v > best:
                    best = v
            for k in range(1, i + 1):
                v = h[i - k][j] + gap(k)
                if v > best:
                    best = v
            h[i][j] = best
    return int(h[n][m])


def main():
    obj = json.load(sys.stdin)
    print(solve(obj["s"], obj["t"]))


if __name__ == "__main__":
    main()
